import java.util.Scanner;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		int b=sc.nextInt();
		if (a>=10*b){
		    System.out.print("YES");
		} 
		else{
		    System.out.print("NO");
		}

	}
}
